function flatten(obj: Record<string, TranslationValue>, prefix = ''): Record<string, string> {
  const result: Record<string, string> = {};
  for (const [key, value] of Object.entries(obj)) {
    const fullKey = prefix ? `${prefix}.${key}` : key;
    if (typeof value === 'string') {
      result[fullKey] = value;
    } else if (typeof value === 'object') {
      Object.assign(result, flatten(value as any, fullKey));
    }
  }
  return result;
}

const SW = flatten(SWAHILI);
const EN = ENGLISH;

const LANG_KEY = 'wajibika-language';

interface I18nContextValue {
  t: (key: string, ...args: string[]) => string;
  language: Language;
  setLanguage: (lang: Language) => void;
}

const I18nContext = React.createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = React.useState<Language>(() => {
    const saved = typeof window !== 'undefined' ? localStorage.getItem(LANG_KEY) : null;
    return (saved === 'en' || saved === 'sw') ? saved : 'sw';
  });

  const persistLanguage = React.useCallback((lang: Language) => {
    setLanguage(lang);
    localStorage.setItem(LANG_KEY, lang);
  }, []);

  const t = React.useCallback(
    (key: string, ...args: string[]) => {
      const dict = language === 'sw' ? SW : EN;
      let val = dict[key];
      if (val === undefined) val = key;
      if (args.length > 0) {
        let i = 0;
        val = val.replace(/\{(\d+)\}/g, () => args[i++] ?? '');
      }
      return val;
    },
    [language],
  );
  return React.createElement(I18nContext.Provider, { value: { t, language, setLanguage: persistLanguage } }, children);
}

export function useI18n(): I18nContextValue {
  const ctx = React.useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}
